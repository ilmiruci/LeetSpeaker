import random


def choice_lan() -> str:
    """
    Запрашивает у пользователя желаемый язык программы
    :return: str: ru or en
    """
    lang: str = ""
    while lang != "ru" and lang != "en":
        lang = input("Выберите язык, русский - ru, английский - en: ")

    return lang


